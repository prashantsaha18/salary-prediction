"""## Evaluate Model Performance"""

from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
import numpy as np
import pandas as pd

# Create a dictionary to store model performance
performance_metrics = {}

# Function to evaluate and store metrics
def evaluate_model(model_name, y_true, y_pred):
    mae = mean_absolute_error(y_true, y_pred)
    mse = mean_squared_error(y_true, y_pred)
    rmse = np.sqrt(mse)
    r2 = r2_score(y_true, y_pred)
    performance_metrics[model_name] = {'MAE': mae, 'MSE': mse, 'RMSE': rmse, 'R2 Score': r2}

# Evaluate Linear Regression
linear_reg_predictions = linear_reg_model.predict(X_test)
evaluate_model('Linear Regression', y_test, linear_reg_predictions)

# Evaluate Decision Tree Regressor
decision_tree_predictions = decision_tree_model.predict(X_test)
evaluate_model('Decision Tree Regressor', y_test, decision_tree_predictions)

# Evaluate SVM Regressor
svm_predictions = svm_model.predict(X_test)
evaluate_model('SVM Regressor', y_test, svm_predictions)

# Evaluate KNN Regressor
knn_predictions = knn_model.predict(X_test)
evaluate_model('KNN Regressor', y_test, knn_predictions)

# Display the performance metrics in a DataFrame
performance_df = pd.DataFrame(performance_metrics).T
display(performance_df)

